package com.sriteja.main;

public class ArithmeticOperators {
	public static void main(String args[]) {
		//declare variables
		int a = 120;
		int b = 50;
		//addition operator
		System.out.println("a+b="+(a+b));
		//subraction operator
		System.out.println("a-b="+(a-b));
		//modulo operator
		System.out.println("a*b="+(a*b));
		//division operator
		System.out.println("a%b="+(a%b));
		
		
		
	}
	

}
